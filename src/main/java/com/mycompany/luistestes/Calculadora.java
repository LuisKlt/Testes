public class Calculadora {
    
    public int somar(int x, int y) {
        return x + y;
    }

    public int subtrair(int x, int y) {
        return x - y;
    }

    public int dividir(int x, int y) {
        return x / y;
    }

    public int multiplicar(int x, int y) {
        return x * y;
    }

    public boolean verificarSeEPar(int x) {
        return x % 2 == 0;
    }
    
}
